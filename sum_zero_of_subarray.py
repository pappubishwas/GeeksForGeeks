class Solution:
    
    #Function to check whether there is a subarray present with 0-sum or not.
    def subArrayExists(self,arr,n):
        ##Your code here
        #Return true or false
        sum_el=0
        mp=set()
        
        for i in range(n):
            sum_el+=arr[i]
            if sum_el==0 or sum_el in mp:
                return True
            mp.add(sum_el)
        return False    


